package gr.unipi.msdn.filmsearch;

import java.util.ArrayList;

public class FireBaseAdapter {

    private ArrayList<FireBaseAdapterMovie> fireBaseAdapter = null;

    public FireBaseAdapter() {
        this.fireBaseAdapter = fireBaseAdapter;
    }

    public FireBaseAdapter(ArrayList<FireBaseAdapterMovie> fireBaseAdapter) {
        this.fireBaseAdapter = fireBaseAdapter;
    }

    public ArrayList<FireBaseAdapterMovie> getFireBaseAdapter() {
        return fireBaseAdapter;
    }

    public void setFireBaseAdapter(ArrayList<FireBaseAdapterMovie> fireBaseAdapter) {
        this.fireBaseAdapter = fireBaseAdapter;
    }
}
